package bibliotecauniversitara;
public class Readers {
    String readerId;
    String CNP;
    String lastName;
    String firstName;
    String telephone;
    String email;
    String faculty;

    public Readers() {
    }

    public Readers(String readerId, String CNP, String lastName, String firstName, String telephone, String email, String faculty) {
        this.readerId = readerId;
        this.CNP = CNP;
        this.lastName = lastName;
        this.firstName = firstName;
        this.telephone = telephone;
        this.email = email;
        this.faculty = faculty;
    }
    
    
    
}
